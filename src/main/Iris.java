package main;

public class Iris {
	
	float sepallength;
	float sepalwidth;
	float petallength;
	float petalwidth;
	String genre;
	
	Iris(float sepallength, float sepalwidth, float petallength, float petalwidth, String genre){
		this.sepallength=sepallength;
		this.sepalwidth=sepalwidth;
		this.petallength=petallength;
		this.petalwidth=petalwidth;
		this.genre=genre;
	}
}
